    private void btnCalcularMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCalcularMouseClicked
        
        double nota1, nota2, nota3, nota4, promedio;
        DecimalFormat prom = new DecimalFormat("#.##");
        nota1 = (double)Spinner1.getValue();
        nota2 = (double)Spinner2.getValue();
        nota3 = (double)Spinner3.getValue();
        nota4 = (double)Spinner4.getValue();
        promedio = (nota1 + nota2 + nota3 + nota4)/4;
        //txtPromedio.setText(String.valueOf(promedio));
        txtPromedio.setText(prom.format(promedio));
        
        if (promedio < 3){
            txtEstado.setText("Embolatao!");
        }else{
            if(promedio <= 4){
               txtEstado.setText("AhÃ­ vas!");
        }else{
                if (promedio <= 5){
                    txtEstado.setText("Flying1");
                }
            }
        }
    }//GEN-LAST:event_btnCalcularMouseClicked

